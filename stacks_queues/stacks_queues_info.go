package stacksqueues

/*
	Stacks and queues are very basic linear data structure.
	They have flexible sizes.

	The main difference stacks and queues is the waf of data remove type.

	Stacks = Last in first out (LIFO)
	When we want to add data we use 'push' method. When we delete to data we use 'pop' method. These of two main methods of the stacks.

	Queues = First in first out (FIFO)
	When we want to add data we use 'Enqueue' method. When we want delete to we use 'Dequeue' method. These of two main methods of the queues.
*/
